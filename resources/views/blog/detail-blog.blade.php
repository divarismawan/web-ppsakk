              <h2 class="entry-title">
                <a>Pura Padharman 
                  Sira Arya Kubontubuh-Kuthawaringin Memang Bukan Di Besakih</a>
              </h2>

              <div class="entry-meta">
                <ul>
                  <li class="d-flex align-items-center"><i class="bi bi-person"></i> <a href="blog-single.html">I Made Pageh Suardhana</a></li>
                  <li class="d-flex align-items-center"><i class="bi bi-clock"></i> <a href="blog-single.html"><time datetime="2020-01-01">3 Maret 2023</time></a></li>
                </ul>
              </div>

              <div class="entry-content">
                <p>
                  Memang pernah ada (beredar) beberapa tulisan (publikasi) yang 
                  menyebutkan adanya Padharman Arya Kubontubuh di kompleks Pura 
                  Besakih. Tetapi penelusuran terhadap kebenaran isi tulisan tersebut 
                  menghasilkan kesimpulan seperti disajikan dalam uraian berikut
                </p>

                <p>
                  Menjelang Karya Eka Dasa Rudra di Besakih, telah diterbitkan buku 
                  yang berjudul “Menyongsong KARYA EKA DASA RUDRA di PURA 
                  BESAKIH”. Buku tersebut disusun oleh I Wayan Surpa dengan kata 
                  sambutan tertanggal 3 Pebruari 1979 dari PARISADA HINDU DHARMA 
                  PUSAT yang ditandatangani oleh Tjokorda Rai Sudharta selaku Ketua III.
                </p>

                <p>Pada halaman 105 Buku tersebut disajikan Bagan Denah Pura Pura Padharman di kompleks Pura Besakih beserta keterangan Bagan 
                  Denah Pura-Pura tersebut pada halaman 106. Pada butir III dalam 
                  keterangan Bagan Denah itu tercantum “Pedarman Kubontubuh”, 
                  sedangkan dalam Bagan Denahnya lokasi Pedharman termaksud 
                  nampak bersebelahan dengan “Pedarman Sukewati.” Darimana informasi 
                  yang mendasari Bagan Denah beserta keterangannya itu diperoleh, 
                  dapat disimak dari uraian “PENJELASAN TENTANG PURA-PURA 
                  PEDHARMAN” pada halaman 69-70 buku tersebut di atas di mana antara 
                  lain terdapat pernyataan seperti kutipan kalimat berikut :
                </p>
                <blockquote>
                  <p>
                    “….. uraian 
                    tentang Pura-Pura Pedharman tersebut di atas hanya disusun 
                    berdasarkan keterangan-keterangan yang diperoleh dari Pemangku-Pemangku di Pura itu, yang mungkin saja belum benar seluruhnya, akan 
                    tetapi minimal dapat memberikan petunjuk dasar bagi para penyiwinya 
                    dalam menyempurnakan riwayat Pura-Pura Pedharman yang menjadi 
                    penyiwiannya, ………”
                  </p>
                </blockquote>

                <p>
                  Selanjutnya uraian penjelasan termaksud 
                  diakhiri dengan kalimat berikut:
                </p>

                <blockquote>
                  <p>
                    “Akhirnya saya memohon maaf bila 
                    dalam uraian tentang Pura-Pura Pedharman tersebut di atas terdapat 
                    kekeliruan serta mohon untuk diperbaiki dan disempurnakan dikemudian 
                    hari.”
                  </p>
                </blockquote>

                <p>
                  Dari dua kalimat kutipan di atas dapat diduga bahwa si penulis 
                  masih belum sepenuhnya yakin akan kebenaran dari apa yang telah 
                  ditulisnya. Dalam buku tersebut diuraikan pula bahwa di areal pura 
                  tersebut antara lain terdapat Meru Tumpang Lima yang dinyatakan 
                  sebagai palinggih Ida Sri Mpu Bujangga dan padharman ini disungsung 
                  oleh keluarga dari Banjar Sengguan Klungkung (Wayan Windia, 1980 : 
                  56). Informasi tersebut sudah tentu mengandung kerancuan karena 
                  antara nama padharman (Arya Kubontubuh) dan yang 
                  disungsung/dinharma(Ida Sri Mpu Bujangga) sama sekali tidak sesuai. 
                  Nampaknya informasi yang rancu tersebut terus-menerus dijadikan 
                  acuan untuk beberapa publikasi berikutnya tanpa membandingkan 
                  dengan sumber informasi dalam kepustakaan-lainnya.
                </p>
                <p>
                  Berdasarkan hasil penelitian DR. R. Goris (1937) tentang Pura 
                  Besakih yang dilengkapi dengan denah Pura Besakih yang digambar oleh 
                  J. Hooykas, menyatakan pura tersebut adalah padharman I Gusti Bendul. 
                  Sedangkan hasil penelitian David J. Stuart- Fox (1987 : 145), 
                  menyimpulkan bahwa petak tersebut, yang terpisah namun masih berada 
                  dalam lingkungan tembok Padharman Sukawati adalah Padharman Ida 
                  Sri Mpu Bujangga yang disungsung oleh kelompok kecil sekitar 60 
                  keluarga yang berasal dari Banjar Celepik, Tojan (Gelgel). Kesimpulan 
                  tersebut didasarkan pada kenyataan bahwa Meru Tumpang Lima yang 
                  ada di sana adalah palinggih Ida Sri Mpu Bujangga.
                  Cara pandang yang juga memperkuat kesimpulan bahwa pura tersebut 
                  bukan Padharman Arya Kubontubuh adalah metode penarikan 
                  kesimpulan yang disepakati dalam Pesamuan. PPSAKK Pusat 
                  Pratisentana Sira Arya Kubontubuh Propinsi Balidalam Pesamuhan yang 
                  diselenggarakan di Nusa Dua pada tanggal 24 Juni 1984, telah 
                  menerapkan pengkajian berdasarkan konsep tripramana, yaitu :
                </p>
                <div class="tab-text">
                  <p>
                    1. Dengan pramana Sastra tah, dikaji adakah tersurat dalam 
                    rontal atau prasasti tentang keberadaan Padharman Arya 
                    Kubontubuh di kompleks Pura Besakih ?
                  </p>
                  <p>
                    2. Dengan pramana Loka tah (loktah), dikaji adakah di antara 
                    warga pratisentana Sira Arya Kubontubuh mengatakan 
                    (mengetahui) tentang keberadaan padharman termaksud di 
                    kompleks Pura Besakih ?
                  </p>
                  <p>
                    3. Dengan pramana Swah tah, dikaji adakah di antara 
                    keluarga/warga pratisentana Sira Arya Kubontubuh sendiri 
                    pernah pedek tangkil (sembahyang) ke pura padharman 
                    termaksud ?
                  </p>
                </div>
                <p>
                  Ternyata hasil pengkajian/penelusuran yang dilakukan menemukan 
                  jawaban : TIDAK terhadap ketiga pertanyaan tersebut di atas, sehingga 
                  Pesamuan Pusat tersebut menyimpulkan bahwa Padharman yang 
                  berada di kompleks Pura Besakih termaksud, BUKAN Padharman Arya 
                  Kubontubuh.</p>
                <p>Di samping keberadaan informasi berdasarkan tulisan (publikasi) 
                  seperti yang telah dibahas di atas, konon pernah ada pula beredar 
                  informasi atau petunjuk lisan yang sampai kepada beberapa orang dari 
                  beberapa warga (termasuk beberapa orang dari Warga Kubontubuh) 
                  yang tidak memiliki Pura Padharman di kompleks Pura Besakih, yang 
                  mengatakan bahwa Pura Padharman Warga termaksud bersama-sama 
                  dengan Pura Padharman dari suatu Warga ‘clan’ tertentu di Pura Besakih.</p>
                <p>Informasi seperti itu tentu saja berpotensi dapat menimbulkan 
                  kebingungan bagi orang-orang yang kurang memahami tentang historis 
                  keberadaan pura-pura Padharman di Besakih. Untuk menghindari hal 
                  tersebut, kepada Warga Pratisentana Sira Arya Kubontubuh-Kuthawaringin, dianjurkan untuk berpedoman pada Pasal 7 Ketetapan 
                  Pesamuan Pusat Khusus Pratisentana Sira Arya Kubontubuh Propinsi 
                  Bali Nomor I/PPK-PSAK/2004 tanggal 25 Januari 2004, yang antara lain 
                  menyatakan:</p>
                <p style="color: #166da7;">Informasi seperti itu tentu saja berpotensi dapat menimbulkan 
                  kebingungan bagi orang-orang yang kurang memahami tentang historis 
                  keberadaan pura-pura Padharman di Besakih. Untuk menghindari hal 
                  tersebut, kepada Warga Pratisentana Sira Arya Kubontubuh-Kuthawaringin, dianjurkan untuk berpedoman pada Pasal 7 Ketetapan 
                  Pesamuan Pusat Khusus Pratisentana Sira Arya Kubontubuh Propinsi 
                  Bali Nomor I/PPK-PSAK/2004 tanggal 25 Januari 2004, yang antara lain 
                  menyatakan:</p>
                <p>Selanjutnya dari sejarah tahun awal dari berdirinya Pura-Pura 
                  Padharman di kompleks Pura Besakih, tercatat tahun 1478 M. menurut 
                  Rontal Padma Bhuwana, tahun 1543M. menurut Babad Sukahet, atau 
                  bahkan tahun 1840-an menurut tulisan dalam Harian Bali Post yang terbit 
                  pada tanggal 16 September 1998 yang ditulis oleh I Gusti Made Warsika 
                  S.H. (seperti sudah dimuat dalam postingan yang lalu yang berjudul : 
                  Keberadaan Pura Padharman <span style="color: red;">Tidak Harus</span> Di Besakih).
                  Bila ketiga tahun itu dibandingkan dengan tahun wafatnya Sira Arya 
                  Kuthawaringin, yang diikuti dengan upacara palebon – baligia –
                  atmapratista/dinharma (disthanakan) di Pura Dalem Tugu, yaitu pada 
                  tahun-tahun akhir dari periode pemerintahan Dalem Ketut Kresna 
                  Kepakisan yang memerintah dalam kurun waktu 1352-1380, maka 
                  logislah kalau memang tidak ada Pura Padharman Sira Arya 
                  Kuthawaringin di kompleks Pura Besakih, oleh karena pembangunan 
                  Pura Padharman di kompleks Pura Besakih itu baru dimulai lebih dari 100 
                  tahun setelah penyelenggaraan upacara mensthanakan (dhinarma) roh 
                  suci Sira Arya Kuthawaringin di Pura Dalem Tugu seperti diuraiakan 
                  dalam buku Alih Aksara Dan Terjemahan dari Raja Purana Pura Dalem 
                  Tugu Pura Kawitan Padharman Sira Arya Kubontubuh-Kuthawaringin.</p>
                <p>Demikianlah berdasarkan hal-hal yang telah diuraikan di atas 
                  dapat disimpulkan bahwa memang tidak ada Pura Padharman Sira 
                  Arya Kuthawaringin atau Sira Arya Kubontubuh di kompleks Pura 
                  Besakih dan tidak pula bersama-sama dalam suatu Pura Padharman 
                  dari suatu warga ‘clan’ tertentu di kompleks Pura Besakih. Pura 
                  Dalem Tugu di Gelgel, Klungkung adalah Pura Kawitan Padharman 
                  Sira Arya Kubontubuh-Kuthawaringin.
                  </p>
